"""
Variaveis são utitlizadas para salvar algo na memoria do computador.
Regras: iniciar variavel com letra maiuscula ( tudo maiusculo é considerado Constante)
O sinal d e = é o operador de atribuiçãi, é utilizado para atribuir
um valor a variavel.
"""

nome = 'seunome'  # criei uma variavel e atriubir ela ao valor de string " seunome"
idade = 20 # criei uma variavel e atribuir a ela o valor de um int 20.
verdade = 20 == 20 # Criei uma variavel e atribuir valores de int, mas que me dara um resultado de boolean.
numero = 5/2 # Criei uma variavel e atruir valores de int, mas que me dara um resultado em float

print(type(verdade)) # = <class 'bool'>
print(type(numero)) # = <class 'float'>
print(type(idade)) # = <class 'int'>
print(type(nome)) # = <class 'str'>

